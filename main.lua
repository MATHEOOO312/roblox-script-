local espEnabled = false
local espBtn = Instance.new("TextButton", frame)
espBtn.Text = "ESP : OFF"
espBtn.Size = UDim2.new(0.8, 0, 0, 35)
espBtn.Position = UDim2.new(0.1, 0, 0, 440)
espBtn.BackgroundColor3 = Color3.fromRGB(150, 50, 200)
